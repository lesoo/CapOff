package com.dongyang.gg.data.item;

public class MyOItemData {

    private String pic;
    private String name;
    private String price;
    private String num;

    public String getItem_pic() {
        return pic;
    }

    public String getItem_name() {
        return name;
    }

    public String getItem_price() {
        return price;
    }

    public String getItem_num() {
        return num;
    }

    public void setItem_pic(String pic) {
        this.pic = pic;
    }

    public void setItem_name(String name) {
        this.name = name;
    }

    public void setItem_price(String price) {
        this.price = price;
    }

    public void setItem_num(String num) {
        this.num = num;
    }
}
